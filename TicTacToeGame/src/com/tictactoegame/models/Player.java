package com.tictactoegame.models;

public class Player {
	private char name;
	private int value;

	public char getName() {
		return name;
	}

	public void setName(char name) {
		this.name = name;
	}

	public int getValue() {
		return value;
	}

	public void setValue(int value) {
		this.value = value;
	}
}